export function normalizeAppLanguage(value: unknown): AppLanguage {
  return value === 'es' ? 'es' : 'en'
}

export function detectAppLanguage(): AppLanguage {
  if (typeof navigator !== 'undefined' && /^es\b/i.test(navigator.language || '')) {
    return 'es'
  }

  return 'en'
}

export function localeForLanguage(language: AppLanguage): string {
  return language === 'es' ? 'es-ES' : 'en-US'
}

export function translate(language: AppLanguage, key: string, values: TemplateValues = {}): string {
  const template = messages[language][key] ?? messages.en[key] ?? key

  return template.replace(/\{(\w+)\}/g, (_match, token: string) => {
    const value = values[token]
    return value === undefined ? `{${token}}` : String(value)
  })
}
